def procesar_datos(datos_texto):
    """Procesa los datos de texto en un DataFrame"""
    lineas = datos_texto.strip().split('\n')
    fechas = []
    numeros_1 = []
    numeros_2 = []
    
    for linea in lineas:
        if linea.strip():
            partes = linea.split('\t')
            if len(partes) == 3:
                try:
                    fechas.append(partes[0])
                    numeros_1.append(int(partes[1]))
                    numeros_2.append(int(partes[2]))
                except ValueError:
                    continue
    
    df = pd.DataFrame({
        'fecha': fechas,
        'numero_1': numeros_1,
        'numero_2': numeros_2
    })
    
    # Invertir para orden cronológico
    df = df.iloc[::-1].reset_index(drop=True)
    df['fecha'] = pd.to_datetime(df['fecha'], format='%d/%m/%Y')
    df['dias'] = (df['fecha'] - df['fecha'].iloc[0]).dt.days
    
    return df

def entrenar_modelos(X, y):
    """Entrena Regresión Lineal y Random Forest"""
    model_lr = LinearRegression()
    model_lr.fit(X, y)
    
    model_rf = RandomForestRegressor(n_estimators=100, random_state=42)
    model_rf.fit(X, y)
    
    return model_lr, model_rf

def hacer_prediccion(model_lr, model_rf, X_pred):
    """Realiza predicción promediando dos modelos"""
    pred_lr = model_lr.predict(X_pred)[0]
    pred_rf = model_rf.predict(X_pred)[0]
    
    return int(np.round((pred_lr + pred_rf) / 2))

